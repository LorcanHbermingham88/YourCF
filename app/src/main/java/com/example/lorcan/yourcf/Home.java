    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_menu) {

        } else if (id == R.id.nav_food) {

            Intent foodActivity = new Intent(Home.this, FoodFacts.class);
            startActivity(foodActivity);
            finish();
        }
        else if (id == R.id.nav_Activity) {

            Intent activity = new Intent(Home.this, Activity.class);
            startActivity(activity);
            finish();

        }else if (id == R.id.nav_howToActivity) {

            Intent activity = new Intent(Home.this, Run.class);
            startActivity(activity);
            finish();



        }
        else if (id == R.id.nav_spirometer) {

            Intent profile = new Intent(Home.this, SpirometerTest.class);
            startActivity(profile);
            finish();

        }
        else if (id == R.id.nav_howTo) {
            Intent sport = new Intent(Home.this, InformationPage2.class);
            startActivity(sport);
            finish();

        }
        else if (id == R.id.nav_profile) {

            Intent profile = new Intent(Home.this, Medical.class);
            startActivity(profile);
            finish();
        }
        else if (id == R.id.nav_reminder) {

            Intent profile = new Intent(Home.this, Calander.class);
            startActivity(profile);
            finish();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
